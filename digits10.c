
unsigned char digits10(unsigned long long n)
{
    return
        (n > 0) +
        (n > 10) +
        (n > 100) +
        (n > 1000) +

        (n > 10000) +
        (n > 100000UL) +
        (n > 1000000UL) +
        (n > 10000000UL) +

        (n > 100000000UL) +
        (n > 1000000000UL) +
             /*4294967295*/
        (n > 10000000000ULL) +
        (n > 100000000000ULL) +

        (n > 1000000000000ULL) +
        (n > 10000000000000ULL) +
        (n > 100000000000000ULL) +
        (n > 1000000000000000ULL) +

        (n > 10000000000000000ULL) +
        (n > 100000000000000000ULL) +
        (n > 1000000000000000000ULL) +
        (n > 10000000000000000000ULL);
             /*18446744073709551615*/
}

